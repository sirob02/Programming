public class main {

    public static void main(String[] args) {
        calculate(1,2);
        calculate(1,2,3);
    }



    public static void calculate(int a,int b){
        int c = a+b;
        System.out.println(a+ " + "+b+" = " +c);




    }
    public static void calculate(int a,int b,int c){
        int d = a+b+c ;
        System.out.println(a+ " + "+b+" + " +c +" = "+d);



    }










}
